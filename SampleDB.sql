CREATE TABLE DEPARTMENT (
    DEPTNO     CHAR(3) PRIMARY KEY,
    DEPTNAME   VARCHAR(36) NOT NULL,
    MGRNO      CHAR(6),
    ADMRDEPT   CHAR(3) NOT NULL,
    LOCATION   VARCHAR(16)
);

CREATE TABLE EMPLOYEE (
    EMPNO      CHAR(6) PRIMARY KEY,
    FIRSTNME   VARCHAR(12) NOT NULL,
    MIDINIT    CHAR(1),
    LASTNAME   VARCHAR(15) NOT NULL,
    WORKDEPT   CHAR(3),
    PHONENO    CHAR(4),
    HIREDATE   DATE,
    JOB        VARCHAR(8),
    EDLEVEL    SMALLINT,
    SEX        CHAR(1),
    BIRTHDATE  DATE,
    SALARY     DECIMAL(9,2),
    BONUS      DECIMAL(9,2),
    COMM       DECIMAL(9,2)
);

CREATE TABLE PROJECT (
    PROJNO     CHAR(6) PRIMARY KEY,
    PROJNAME   VARCHAR(24),
    DEPTNO     CHAR(3),
    RESPEMP    CHAR(6),
    PRSTAFF    DECIMAL(5,2),
    PRSTDATE   DATE,
    PRENDATE   DATE
);

CREATE TABLE PROJ_ACT (
    PROJNO CHAR(6),
    ACTNO  SMALLINT,
    ACTDESC VARCHAR(20),
    PRIMARY KEY (PROJNO, ACTNO)
);

CREATE TABLE EMP_ACT (
    EMPNO  CHAR(6),
    PROJNO CHAR(6),
    ACTNO  SMALLINT,
    EMPTIME DECIMAL(5,2),
    PRIMARY KEY (EMPNO, PROJNO, ACTNO)
);

